def write_fs_files(output_dir, subid):
    """Touches an empty file for each FreeSurfer file in the output directory."""
    for file_pattern in fs_files:
        fpath = Path(file_pattern.format(output_dir=output_dir, subid=subid))
        if file_pattern.endswith('/'):
            # For directories, only create if it doesn't exist
            if not fpath.exists():
                fpath.mkdir(parents=True, exist_ok=True)
        else:
            # For files, ensure parent directory exists and create file
            if not fpath.parent.exists():
                fpath.parent.mkdir(parents=True, exist_ok=True)
            if not fpath.exists():
                fpath.touch()
